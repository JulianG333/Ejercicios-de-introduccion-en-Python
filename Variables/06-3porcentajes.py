# Solicitar al usuario un número
numero = float(input("Por favor, ingresa un número: "))
porcentaje30 = numero * 0.30
porcentaje60 = numero * 0.60
porcentaje90 = numero * 0.90
print(f"El 30% de {numero} es: {porcentaje30}")
print(f"El 60% de {numero} es: {porcentaje60}")
print(f"El 90% de {numero} es: {porcentaje90}")