#Se pide al usuario los 5 números
primero = int(input("Ingrese un primer número: "))
segundo = int(input("Ingrese un segundo número: "))
tercero = int(input("Ingrese un tercer número: "))
cuarto = int(input("Ingrese un cuarto número: "))
quinto = int(input("Ingrese un quinto número: "))
#Se calcula el promedio de los numeros ingresados
Promedios = (primero + segundo + tercero + cuarto + quinto)/5
print(f"El promedio de los 5 números es {Promedios}")